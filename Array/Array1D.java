package Array;

public class Array1D {
    public static void main(String[] args) {
        
        // int nums[] = {5,8,2,1}
        int nums[] = new int[4];
        
        nums[0] = 5;
        nums[1] = 3;
        nums[2] = 4;
        nums[3] = 7;

        for(int i=0;i<4;i++){
            System.out.println(nums[i]);
        }
    }
}
